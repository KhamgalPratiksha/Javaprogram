package com.area;

public class SwapNumbersUsingThirdVariable {

	public static void main(String[] args) {
	int a,b,temp;
	a=20;
	b=40;
	System.out.println("Before interchanging a="+a+" and b="+b);
	
	temp=a;
	a=b;
	b=temp;
	
	System.out.println("After swipping a="+a+" and b="+b);

	}

}
